import re


email_regex_pattern = "^([a-z0-9]+[.-_])*[a-z0-9]+@[a-z0-9-]+(\.[a-z]{2,})+"
email_regex = re.compile(email_regex_pattern)


def email_valid(email: str):
    return bool(re.fullmatch(email_regex, email))


def email_invalid(email: str):
    return not email_valid(email)


password_regex_description = "Password must have from 8-32, one uppercase letter, one lowercase letter, one number and one special character"
password_regex_pattern = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@#$!%*?&])[A-Za-z\d@#$!%*?&]{8,32}$"
password_regex = re.compile(password_regex_pattern)


def password_valid(password: str):
    return bool(re.fullmatch(password_regex, password))


def password_invalid(password: str):
    return not password_valid(password)


